public class HspStat {

    String currentHsp;
    int bruteScore;
    double bitScore;
    double eValue;
    int sequenceStart;
    int databaseStart;

    public HspStat(String currentHsp,int bruteScore, double bitScore, double eValue, int sequenceStart, int databaseStart){
        this.currentHsp = currentHsp;
        this.bruteScore = bruteScore;
        this.bitScore = bitScore;
        this.eValue = eValue;
        this.sequenceStart = sequenceStart;
        this.databaseStart = databaseStart;

    }





}
